// Quiz
// What is the difference between var and let?
// Var is function scoped, that can be reassigned, redeclared and can mutate, while Let is block scoped, can be reassigned, can Not be redeclared and can mutate.

// What is the difference between var and const?
// Var is function scoped, that can be reassigned, redeclared and can mutate, while Const is block scoped, can Not be reassigned, can Not be redeclared and can mutate.

// What is the difference between let and const?
// Let is block scoped, can be reassigned, can Not be redeclared and can mutate, while Const is block scoped, can Not be reassigned, can Not be redeclared and can mutate.

